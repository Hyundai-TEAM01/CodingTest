SELECT T1.ANIMAL_ID, ANIMAL_TYPE, NAME 
FROM (SELECT ANIMAL_ID FROM ANIMAL_INS WHERE SEX_UPON_INTAKE LIKE 'Intact%') T1,
    ANIMAL_OUTS T2
WHERE T1.ANIMAL_ID = T2.ANIMAL_ID AND SEX_UPON_OUTCOME NOT LIKE 'Intact%' ORDER BY T1.ANIMAL_ID;
