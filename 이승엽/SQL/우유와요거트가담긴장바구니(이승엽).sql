SELECT distinct(t1.CART_ID) 
FROM (SELECT CART_ID FROM CART_PRODUCTS WHERE NAME = 'Milk') t1,
        (SELECT CART_ID FROM CART_PRODUCTS WHERE NAME = 'Yogurt') t2
WHERE t1.CART_ID = t2.CART_ID ORDER BY t1.CART_ID;